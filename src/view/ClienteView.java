    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        int confirmacao = JOptionPane.showConfirmDialog(null, "Confirma exclusão ?","Exclusão de cliente",JOptionPane.YES_NO_OPTION);
        
        if(confirmacao==0){
            daoCliente.excluir(Integer.parseInt(tf_codigo.getText()));
            jtClientes.setModel(new ClienteTableModel(daoCliente.buscarTodos()));
            this.limparCampos();
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void botaoNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoNovoActionPerformed
        this.limparCampos();
    }//GEN-LAST:event_botaoNovoActionPerformed

    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        if(tf_cExclusivo.getText().equals("") || tf_cpf.getText().equals("") || 
                tf_nome.getText().equals("") || tf_email.getText().equals("")
                || tf_telefone.getText().equals("") || tf_idade.getText().equals("")){
            JOptionPane.showMessageDialog(null, "É mecessário preencher todos os campos", "Aviso",JOptionPane.WARNING_MESSAGE);
        } else {
            cliente.setNome(tf_nome.getText());
            cliente.setCPF(tf_cpf.getText());
            cliente.setIdade(Integer.parseInt(tf_idade.getText()));
            cliente.setEmail(tf_email.getText());
            cliente.setCartaoExclusivo(Integer.parseInt(tf_cExclusivo.getText()));
            cliente.setTelefone(tf_telefone.getText());
            
            if(tf_codigo.getText().trim().equals("")){
                daoCliente.inserir(cliente);
            } else {
                cliente.setCodigo(Integer.parseInt(tf_codigo.getText()));
                daoCliente.alterar(cliente);
            }
            jtClientes.setModel(new ClienteTableModel(daoCliente.buscarTodos()));
            this.limparCampos();
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void jtClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtClientesMouseClicked
        tf_codigo.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), ClienteTableModel.COL_CODIGO).toString());
        tf_nome.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), ClienteTableModel.COL_NOME).toString());
        tf_cExclusivo.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), ClienteTableModel.COL_CARTAO_EXCLUSIVO).toString());
        tf_cpf.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), ClienteTableModel.COL_CPF).toString());
        tf_email.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), ClienteTableModel.COL_EMAIL).toString());
        tf_telefone.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(),ClienteTableModel.COL_TELEFONE).toString());
        tf_idade.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), ClienteTableModel.COL_IDADE).toString());
        botaoExcluir.setEnabled(true);
    }//GEN-LAST:event_jtClientesMouseClicked

