    private void botaoFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFiltrarActionPerformed
        String campo;
        System.out.println("HEY !");
        if(rbPesqNome.isSelected()){
            campo = "nome";
        }else if(rbPesqEmail.isSelected()){
            campo = "email";
        } else if(rbPesqTelefone.isSelected()){
            campo = "telefone";
        } else {
            campo = "cpf";
        }
        jtClientes.setModel(new ClienteTableModel(daoCliente.buscarPorFiltro(campo, tfPesquisa.getText())));
        this.limparCampos();
    }//GEN-LAST:event_botaoFiltrarActionPerformed

